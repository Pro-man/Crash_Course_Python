# Make a list of the numbers from one to one million.
# Use for loop to print the numbers


# empty list
numbers = []
# for loop to count to one million
for num in range(1, 1000001):
    # put the num value in a variable
    number = num
    # insert numbers in the list
    numbers.append(number)
    # print the numbers 
    print(numbers)